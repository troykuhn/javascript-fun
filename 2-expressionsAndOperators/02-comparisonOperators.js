// Equality

// equal operator

'3' == 3; // -> true

// strict equality operator
'3' === 3; // -> false

//Opposite operator

'3' !== 3; // true .. more strict

'3' != 3; //false

// Greater than

3 > 4; // false

//Less than
3 < 4; // true 

// Greater than or equal to

3 >= 4; // false

//Less than or equal to
3 <= 4; // true

